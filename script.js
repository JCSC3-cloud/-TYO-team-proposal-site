function createAmbientMusic() {
  const Context = window.AudioContext || window.webkitAudioContext;
  if (!Context) return null;
  const context = new Context();
  const master = context.createGain();
  master.gain.value = 0.0001;
  master.connect(context.destination);

  const delay = context.createDelay();
  delay.delayTime.value = 0.32;
  const feedback = context.createGain();
  feedback.gain.value = 0.22;
  delay.connect(feedback);
  feedback.connect(delay);
  delay.connect(master);

  const filter = context.createBiquadFilter();
  filter.type = "lowpass";
  filter.frequency.value = 1350;
  filter.Q.value = 0.72;
  filter.connect(delay);
  filter.connect(master);

  const freqs = [130.81, 196, 261.63, 329.63];
  const oscillators = freqs.map((freq, index) => {
    const osc = context.createOscillator();
    const gain = context.createGain();
    osc.type = index % 2 ? "triangle" : "sine";
    osc.frequency.value = freq;
    gain.gain.value = index === 0 ? 0.032 : 0.014;
    osc.connect(gain);
    gain.connect(filter);
    osc.start();
    return { osc, gain };
  });

  const lfo = context.createOscillator();
  const lfoGain = context.createGain();
  lfo.frequency.value = 0.045;
  lfoGain.gain.value = 160;
  lfo.connect(lfoGain);
  lfoGain.connect(filter.frequency);
  lfo.start();

  const melodyGain = context.createGain();
  melodyGain.gain.value = 0.18;
  melodyGain.connect(delay);
  melodyGain.connect(master);
  const scale = [392, 440, 523.25, 587.33, 659.25, 587.33, 523.25, 440, 392, 329.63, 392, 523.25];
  let step = 0;
  const playNote = () => {
    if (!musicToggle?.classList.contains("active")) return;
    const now = context.currentTime;
    const osc = context.createOscillator();
    const noteGain = context.createGain();
    osc.type = "triangle";
    osc.frequency.setValueAtTime(scale[step % scale.length], now);
    noteGain.gain.setValueAtTime(0.0001, now);
    noteGain.gain.exponentialRampToValueAtTime(0.09, now + 0.018);
    noteGain.gain.exponentialRampToValueAtTime(0.0001, now + 0.58);
    osc.connect(noteGain);
    noteGain.connect(melodyGain);
    osc.start(now);
    osc.stop(now + 0.62);
    step += 1;
  };
  const melodyTimer = window.setInterval(playNote, 430);

  return { context, master, oscillators, lfo, melodyTimer };
}

